import random
print(random.randint(5, 20))  # line1 The smallest number I could have seen is 5. and the largest is 20.
print(random.randrange(3, 10, 2))  # line2 The smallest number I could have seen is 3. and the largest is 9.
# line 2 could not have produced a 4, because the step is 2.
print(random.uniform(2.5, 5.5)) # line2 The smallest number I could have seen is 2.500000000000000.
# and the largest is 5.500000000000000.